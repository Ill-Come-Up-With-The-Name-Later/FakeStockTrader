package utilities;

/**
 * Represents the status of the price
 * of an <code>Asset</code>
 */
public enum Status {

    /**
     * Asset's price is increasing
     */
    INCREASING,

    /**
     * Asset's price is decreasing
     */
    DECREASING,

    /**
     * Asset's price is remaining
     * relatively the same
     */
    STATIC
}
